<div class="jumbotron" style="width: 50%;margin-left: 400px;background-color:pink;border-radius: 20px">
  <h2 style="text-align: center;font-size: 40px;margin-top: -60px;color:black; text-decoration:underline;">Summary</h2>
  <p style="font-size: 20px;">
This Project will train groups of 10 poor single mothers, widows, divorcees, school & college dropouts in different nodes of Navi Mumbai in designing jute woven bags, home linen, ladies dresses and the manufacturing process of paper artifacts. Empowering groups with stitching machines, entrepreneur workshops and making them financially independent contributing members of their family and community for an enriched happy society
  </p><br><br><br>
  <h2 style="text-align: center;font-size: 40px;margin-top: -60px; text-decoration:underline;">Challenge</h2>
  <p style="font-size: 20px;">
Poor women, single mothers, widows & divorcees don't have assured sources of earning & livelihood. Most are overworked, under-paid, maid servants earning 10$ monthly from each home. With no work in the afternoon, a few are working in civil construction sites earning a pittance of what a man would earn. Migratory laborers, especially women, are fleeced, ill-treated and subject themselves to 10 hours of manual work a day due to fear of losing their job. Some are also forced into the flesh trade.
  </p><br><br><br>
  <h2 style="text-align: center;font-size: 40px;margin-top: -60px; text-decoration:underline;">Solution</h2>
  <p style="font-size: 20px;">
100 women in groups of 10 will get design & manufacturing skills to make woven jute bags, home linen and ladies dresses. One set of equipment will also be given to each group. They will be trained in entrepreneurial, business management and marketing skills, and will have an assured regular income source for a better living. Besides, all these ill-treated women will earn respect from their relatives and will have enhanced confidence, self-sustaining abilities & socio-economic improvement
  </p>
