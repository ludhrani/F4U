<div class="jumbotron" style="width: 50%;margin-left: 400px;background-color:pink;border-radius: 20px">
  <h2 style="text-align: center;font-size: 40px;margin-top: -60px;color:black;text-decoration: underline;">Summary</h2>
  <p style="font-size: 20px;">
Project will form First-Responder Youth Brigade in different nodes of Navi Mumbai to help in any disaster calling for emergency response from trained First Responders, including attending to sudden cardiac arrest victims in their neighborhood. First-Responder will assist by giving first-aid to the victim(s) in the Golden Minutes of the incident & save hapless victims from death. Besides learning 'personal safety' first, it would encourage them to respond to man-made disasters in their vicinity
  </p><br><br><br>
  <h2 style="text-align: center;font-size: 40px;margin-top: -60px;text-decoration: underline;">Challenge</h2>
  <p style="font-size: 20px;">
Program will train 50 youth to become 'First-Responder Youth Brigade' in each node of Navi Mumbai starting with Airoli node. Navi Mumbai-Uran-Thane belt is in an earthquake zone-3 area; besides it is densely populated by people & industries that may result in man-made disasters too. Untrained bystander & rescuer is unable or unwilling to help hapless victims of any disaster leading to preventable death. Many women/children are main casualties in most disasters, calling for immediate ERT programs
  </p><br><br><br>
  <h2 style="text-align: center;font-size: 40px;margin-top: -60px;text-decoration: underline;">Solution</h2>
  <p style="font-size: 20px;">
50 Youth from each node of Navi Mumbai would be trained to learn ABCs of First-Aid, Effective CPR, & Emergency Response techniques & be a part of First-Responder 'Youth Brigade' in that node. 'Youth Brigade' shall attend to casualties during the Golden Minutes of an incident. Women & children are major casualties in any disaster; women would be trained in ERT that would help them save their kith/kin too. Emergency Response training at an young age would be an Asset, for life-long, to save lives
  </p>
